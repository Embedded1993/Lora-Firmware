void SleepMode(void)
{
	/* Suspend SysTick */
	HAL_SuspendTick();

	/* Sleep Mode */
	HAL_PWR_EnterSLEEPMode(0, PWR_SLEEPENTRY_WFI);

	/* Resume SysTick When System Wake-up */
	HAL_ResumeTick();
}

void WakeUp(void)
{
	if(__HAL_PWR_GET_FLAG(PWR_FLAG_SB)) // check system is wake up
	{
		__HAL_PWR_CLEAR_FLAG(PWR_FLAG_WU | PWR_FLAG_SB);
	}
}
